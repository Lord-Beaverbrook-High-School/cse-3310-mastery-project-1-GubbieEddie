    //this method will include:
    //picking the pivot, establishing the low, a loop that moves smaller elements to the left of the
    //partition, moves pivot one element smaller and returns the position
public static int partition(int[] arr, int low, int high){
        //this is choosing the pivot
        int pivot = arr[high];

        //this is the index of the lowest element
        int z = low - 1;

        //this traverses the array and moves all the smaller elements to the left side of the pivot
        //elements from low to h get smaller after every call
        for(int h = low; h <= high - 1; h++){
            if (arr[h] < pivot) {
                z++;
                arraySwap(arr, z, h);
            }
        }

        //this changes the pivot to one index smaller as well as states it's postion
        arraySwap(arr,z + 1, high);
        return z + 1;
}

